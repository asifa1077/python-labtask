# Demonstrating how to output variables using print() function

x = "Sylhet International University (SIU)"
print(x)  # Output: Sylhet International University (SIU)

# Output multiple variables using commas
x = "Shamimabad"
y = "Bagbari"
z = "Sylhet"
print(x, y, z)  # Output: Shamimabad Bagbari Sylhet