# Example of string concatenation using + operator

# Version with spaces - Correct result
x = "Python "
y = "is "
z = "awesome"
print("With space:", x + y + z)  # Output: Python is awesome

#Version without spaces - Incorrect result
x = "Python"
y = "is"
z = "awesome"
print("Without space:", x + y + z)  # Output: Pythonisawesome