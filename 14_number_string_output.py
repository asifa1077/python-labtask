# Demonstration of + operator with numbers and strings

# Example 1: + works as mathematical operator with numbers
x = 7
y = 10
print("Sum of numbers:", x + y)  # Output: 17

# Example 2: + operator does not work between string and number
x = 7
y = "Nilyma"
# print(x + y)  # This will raise an error: TypeError

# Example 3: Use commas to combine different types safely
print("Mix of number and string with comma:", x, y)  # Output: 7 Nilyma